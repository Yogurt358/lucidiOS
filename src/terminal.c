void reset(struct limine_framebuffer* _fb) {
    column = 0;
    line = 0;

    for (size_t y = 0; y <_fb->height; y++) {
        for(size_t x = 0; x < _fb->width; x++) {
            //kprintf("%x, %x\n", x, y);
            draw_pixel(_fb, x, y, RGB32_BLACK);
        }
    }
}
void scrollUp(struct limine_framebuffer* _fb) {
    volatile uint32_t *fb_ptr = _fb->address;
    uint64_t pitch_px = _fb->pitch / 4;

    // Shift every row up by one (start from y=1, copy to y-1)
    for (size_t y = 1; y < _fb->height; y++) {
        for (size_t x = 0; x < _fb->width; x++) {
            fb_ptr[(y - 1) * pitch_px + x] = fb_ptr[y * pitch_px + x];
        }
    }
    // Clear the newly freed last row
    for (size_t x = 0; x < _fb->width; x++) {
        fb_ptr[(_fb->height - 1) * pitch_px + x] = RGB32_BLACK;
    }
}

void newLine(struct limine_framebuffer* _fb) {
    if (line < _fb->height - 1) {
        line++;
        column = 0;
    }
    else {
        scrollUp(_fb);
        column = 0;
    }
}

void draw_pixel (struct limine_framebuffer* _fb, size_t x, size_t y, uint32_t color) {
    //uint64_t width = _fb->width;
    //uint64_t height = _fb->height;
    volatile uint32_t *fb_ptr = (uint32_t*)(_fb->address);

    fb_ptr[y*(_fb->pitch/4)+x] = color;

}

void draw_character(struct limine_framebuffer* _fb, size_t x, size_t y, char c, uint32_t color) {

    for (size_t j = 0; j < 16; j++) {
        uint8_t glyph = font[(unsigned char)(c)][j]; // glyph starts at 0b00000000

        for (size_t i = 0; i < 8; i++) {
            if (glyph & (0x80 >> i)) {draw_pixel(_fb, (x+i), (y+j), color);}
            else {draw_pixel(_fb, (x+i), (y+j), RGB32_BLACK);}
        }
    }
}

void draw_sentence(struct limine_framebuffer* _fb, char* s, uint32_t color) {
    // Calculate max columns and lines based on 8x16 font
    uint16_t max_cols = _fb->width / 8;
    //uint16_t max_lines = _fb->height / 16;

    while(*s != '\0') {
        switch(*s) {
            case '\n':
                newLine(_fb);
                break;
            case '\r':
                column = 0;
                break;
            case '\t': {
                uint16_t tabLen = 4;
                for(int x = 0; x < tabLen; x++) {
                    if (column >= max_cols) {
                        newLine(_fb);
                    }
                    draw_character(_fb, column * 8, line * 16, ' ', color);
                    column++;
                }
                break;
            }
            default:
                if (column >= max_cols) {
                    newLine(_fb);
                }
                // Convert grid (column, line) to pixels (column*8, line*16)
                draw_character(_fb, column * 8, line * 16, *s, color);
                column++;
                break;
        }
        s++;
    }
}

uint32_t plasma_pixel(size_t x, size_t y, uint32_t t, uint64_t W, uint64_t H) {
    uint8_t sx = (uint8_t)((x*255) / W);
    uint8_t sy = (uint8_t)((y*255) / H);

    // Three independent sine waves with different spatial frequencies
    uint8_t v1 = sine_lut[(sx+t*1) & 0xFF];
    uint8_t v2 = sine_lut[(sy+t*1) & 0xFF];
    uint8_t v3 = sine_lut[((sx/2+sy/2)+t*3) & 0xFF];

    uint8_t h = (uint8_t)((v1 + v2 + v3) / 3);
    uint8_t r = sine_lut[(h) & 0xFF];
    uint8_t g = sine_lut[(h+ 85) & 0xFF]; // 85  = 256/3
    uint8_t b = sine_lut[(h+170) & 0xFF]; // 170 = 2*256/3

    return ((uint32_t)r << 16) | ((uint32_t)g << 8) | b;
}

void copy_screen(struct limine_framebuffer* _fb) {
    extern bool err;
    
    screen_size = (_fb->width)*(_fb->height)*4;
    backup = (uint32_t*)kmalloc(screen_size);
    
    if (backup == NULL) {
        kprintf("Failed to allocate screen backup\n");
        kprintf("Needed backup size:%d\n", screen_size);
        err = 0;
        return;
    }

    memcpy(backup,_fb->address,screen_size);
    
    screen_saver(_fb);
    
    memcpy(_fb->address, backup, screen_size);

    kfree(backup);
    backup = NULL;
    

    

}

void screen_saver(struct limine_framebuffer* _fb) {
    extern volatile uint64_t ticks;
    uint32_t t = 0;

    for (;;) {
        if (ticks <= 1000) { t = 0; break;} // how much time pass before screen saver launches

        for (size_t i = 0; i < _fb->height; i++)
            for (size_t j = 0; j < _fb->width; j++)
                draw_pixel(_fb, j, i, plasma_pixel(j, i, t, _fb->width, _fb->height));
        t+=2;
    }
}

void draw_logo(struct limine_framebuffer *_fb, uint32_t fg_color, uint32_t bg_color)
{
    // Final on-screen size after scaling
    const size_t draw_w = LOGO_WIDTH  * LOGO_SCALE;
    const size_t draw_h = LOGO_HEIGHT * LOGO_SCALE;

    
    if (_fb->width < draw_w || _fb->height < draw_h) {
        return;
    }

    // Center on screen
    const size_t origin_x = (_fb->width  - draw_w) / 2;
    const size_t origin_y = (_fb->height - draw_h) / 2;

    for (size_t sy = 0; sy < LOGO_HEIGHT; sy++) {
        for (size_t sx = 0; sx < LOGO_WIDTH; sx++) {
            // Locate the bit for source pixel (sx, sy)
            size_t bit_index = sy * LOGO_WIDTH + sx;
            uint8_t byte = logo_data[bit_index >> 3];   // /8
            uint8_t mask = 0x80 >> (bit_index & 7);     // %8
            uint32_t color = (byte & mask) ? fg_color : bg_color;

            // Compute top-left of this source pixel's screen block
            size_t px = origin_x + sx * LOGO_SCALE;
            size_t py = origin_y + sy * LOGO_SCALE;

        
            for (size_t dy = 0; dy < LOGO_SCALE; dy++) {
                for (size_t dx = 0; dx < LOGO_SCALE; dx++) {
                    draw_pixel(_fb, px + dx, py + dy, color);
                }
            }
        }
    }
}

